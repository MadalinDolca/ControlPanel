package autentificari;

public class Autentificari {

    int id;
    String numeCont;
    String numePC;
    String idPC;
    String accesare;

    public Autentificari(int id, String numeCont, String numePC, String idPC, String accesare) {
        this.id = id;
        this.numeCont = numeCont;
        this.numePC = numePC;
        this.idPC = idPC;
        this.accesare = accesare;
    }
}
